package week13d04extended;

class EmployeeForTemplateExtended {
    private String name;
    int yearOfBirth;

    public EmployeeForTemplateExtended(String name, int yearOfBirth) {
        this.name = name;
        this.yearOfBirth = yearOfBirth;
    }

    public String getName() {
        return name;
    }

    public int getYearOfBirth() {
        return yearOfBirth;
    }
}
